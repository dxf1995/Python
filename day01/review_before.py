name = 'qian'
name_last = 'pingping'

all_name = name + ' ' + name_last
# 首字母大写
print( all_name.title() )

# 全大写
print(all_name.upper())

# 全小写
print(all_name.lower())

# 删除空格
name_rstrip = all_name.rstrip()
print(name_rstrip)

# \t 添加空白 \n　换行
print('Enjoy my happinessalone')
print('Enjoy my happiness\talone')

print('Enjoy\n \tmy\n\t\thappiness\n')
